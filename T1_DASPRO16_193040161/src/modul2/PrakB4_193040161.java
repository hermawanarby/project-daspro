package modul2;

public class PrakB4_193040161 {
	public static void main(String[] args) {
		int nilaiA, nilaiB;
		
		nilaiA = 5;
		nilaiB = 6;
		
		System.out.println(nilaiA < nilaiB); // hasilnya akan TRUE
		System.out.println(nilaiA > nilaiB); // hasilnya akan FALSE
		System.out.println(nilaiA <= 5); // hasilnya akan TRUE
		System.out.println(nilaiB >= 6); // hasilnya akan TRUE
		System.out.println(nilaiA == nilaiB); // hasilnya akan FALSE
		System.out.println(nilaiA != nilaiB); // hasilnya akan TRUE
	}
}
